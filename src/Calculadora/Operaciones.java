public class Operaciones {
    public int sumaNumeros(int x, int y)
    {
        int resultado= x+y;
        return resultado;
    }
    
    public int restaNumeros(int x, int y)
    {
        int resultado= x-y;
        return resultado;
    }
    
    public int multiplicaNumeros(int x, int y)
    {
        int resultado= x*y;
        return resultado;
    }
    
    public int divideNumeros(int x, int y)
    {
   
        int resultado= x/y;
        return resultado;
    } 
                   
}
   

